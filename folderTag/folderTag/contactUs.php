    $(document).ready(function(){            
        $("#contact-btn").click(function(event){
            event.preventDefault();
            var msg = "";
            var user_name = $("#user_name").val().trim();
            var user_mail = $("#user_mail").val();
            var user_phone = $("#user_phone").val();
            var user_message = $("#user_message").val().trim();

            // ============= name validation ===========
            if(user_name === ""){
                msg = "**Please enter your name.";
                $('#name_validation').php(msg);
            }else{
                $('#name_validation').php("");
            }

            // ============= email validation ===========

            if(user_mail === ""){
                msg = "**Please enter your email.";
                $('#email_validation').php(msg);
            }else{
                var emailReg = /^([\w-\.]+@([\w-]+\.)+[\w-]{2,4})?$/;
                if(!emailReg.test(user_mail)){
                    msg = "**Please enter valid email";
                    $('#email_validation').php(msg);
                }else{
                    $('#email_validation').php("");
                }   
            }

            //  ============= phone validation =============
            if(user_phone === ""){
                msg = "**Please enter your number";
                $('#phone_validation').php(msg);
            }else{
                var phoneReg = /^\+?\d{1,4}?[-.\s]?\(?\d{1,3}?\)?[-.\s]?\d{1,4}[-.\s]?\d{1,4}[-.\s]?\d{1,9}$/ ;
                if(!phoneReg.test(user_phone)){
                    msg = "**Please enter valid Number";
                    $('#phone_validation').php(msg);
                }else{
                    $('#phone_validation').php("");
                }   
            }
            // ============ message should not be empty ==================
            if(user_message === ""){
                msg = "**Please enter any msg.";
                $('#message_validation').php(msg);
            }else{
                $('#message_validation').php("");
            }

            // console.log(msg);
            // ============= AJAX CODE TO RE-DIRECT TO THE SERVER AFTER VALIDATION ==============
            if(msg === ""){
                console.log("good to go.");
